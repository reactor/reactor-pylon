import {
  TestComponentBuilder,
  describe,
  expect,
  injectAsync,
  it
} from 'angular2/testing';
import {Component} from 'angular2/core';
import {DOM} from 'angular2/src/platform/dom/dom_adapter';
import {LogComponent} from './log.component';

export function main() {
  describe('System component', () => {
    it('should work',
      injectAsync([TestComponentBuilder], (tcb: TestComponentBuilder) => {
        return tcb.createAsync(TestComponent)
          .then((rootTC) => {
            let logDOMEl = rootTC.debugElement.children[0].nativeElement;
            expect(DOM.querySelectorAll(logDOMEl, 'h2')[0].textContent).toEqual('Features');
          });
      }));
  });
}

@Component({
  selector: 'test-cmp',
  directives: [LogComponent],
  template: '<reactor-log></reactor-log>'
})
class TestComponent {}
